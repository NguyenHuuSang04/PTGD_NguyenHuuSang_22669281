import React, { useReducer, useState } from 'react';

// Reducer xá»­ lÃ½ cÃ¡c phÃ©p tÃ­nh
const calculatorReducer = (state, action) => {
  const { type, payload } = action;
  const { number1, number2 } = payload;

  switch (type) {
    case 'ADD':
      return { result: number1 + number2 };// thá»±c hiá»‡n phÃ©p cá»™ng
    case 'SUBTRACT':
      return { result: number1 - number2 }; // thá»±c hiá»‡n phÃ©p trá»«
    case 'MULTIPLY':
      return { result: number1 * number2 };
    case 'DIVIDE':
      return { result: number2 !== 0 ? number1 / number2 : 'KhÃ´ng chia Ä‘Æ°á»£c cho 0' };
    default:
      return state;
  }
};

function App() {
  const [state, dispatch] = useReducer(calculatorReducer, { result: 0 });
  const [number1, setNumber1] = useState(0);
  const [number2, setNumber2] = useState(0);

  const handleCalculate = (type) => {
    dispatch({
      type,
      payload: {
        number1: parseFloat(number1),
        number2: parseFloat(number2)
      }
    });
  };

  return (
    <div style={{ padding: 20 }}>
      <h2>ðŸ§® MÃ¡y tÃ­nh Ä‘Æ¡n giáº£n (useReducer)</h2>
      <input
        type="number"
        value={number1}
        onChange={(e) => setNumber1(e.target.value)}
        placeholder="Sá»‘ thá»© nháº¥t"
      />
      <input
        type="number"
        value={number2}
        onChange={(e) => setNumber2(e.target.value)}
        placeholder="Sá»‘ thá»© hai"
        style={{ marginLeft: 10 }}
      />
      <div style={{ marginTop: 10 }}>
        <button onClick={() => handleCalculate('ADD')}>+</button>
        <button onClick={() => handleCalculate('SUBTRACT')}>-</button>
        <button onClick={() => handleCalculate('MULTIPLY')}>Ã—</button>
        <button onClick={() => handleCalculate('DIVIDE')}>Ã·</button>
      </div>
      <h3 style={{ marginTop: 20 }}>Káº¿t quáº£: {state.result}</h3>
    </div>
  );
}

export default App;
